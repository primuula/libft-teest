#include<unistd.h>

void	putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putendl_fd(char *s, int fd)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
	{
		putchar(s[i]);
	i++;
	}
	write(fd, "\n", 1);
}
